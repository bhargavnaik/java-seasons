package flowcontrolstatements;

public class SwitchDemo1 {

	public static void main(String[] args) {
		
		int x = 10;
		
		switch (x) {
		case 10:  
			System.out.println("x is 10");
			break;
		case 20:  
			System.out.println("x is 10");
			break;
		case 30:  
			System.out.println("x is 10");
			break;
		default:
			System.out.println("x is neither  20 nor 30");
			break;
		}

	}

}
